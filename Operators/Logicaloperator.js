//logical operators
let isSunny = true;
let isWarm = false;

// Logical AND (&&)
let isGoodWeather = isSunny && isWarm;
console.log("Is it good weather?", isGoodWeather); 

// Logical OR (||)
let isIdealWeather = isSunny || isWarm;
console.log("Is it ideal weather?", isIdealWeather);

// Logical NOT (!)
let isNotSunny = !isSunny;
console.log("Is it sunny?", isNotSunny);